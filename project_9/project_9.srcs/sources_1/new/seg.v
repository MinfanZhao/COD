module seg(
input clk,
input rst_n,
input [15:0]q_a,
output reg [7:0]SEG,
output reg [2:0]data,
output [15:0]LED
    );
    reg [15:0]count;
   assign LED=q_a;
    always@(posedge clk or negedge rst_n)
    if(!rst_n)
    begin
    count<=0;
    data<=0;
    end
    else if (count==16'd49999)
    begin
    count<=0;
    if( data==3'b011)
    data<=0;
    else 
    data<=data+1'b1;
    end
    else
    count<=count+1'b1;
    
    always@(posedge clk)
    case(data)
    3'b000:
           case(q_a[3:0])
           4'b0000: SEG[7:0]=8'b11000000;
           4'b0001: SEG[7:0]=8'b11111001;
